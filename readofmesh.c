#include "header.h"
#include "locations.h"

#define FUNCTION_NAME "ReadOFMesh:"

/*****************************************************************************/

void ReadOFMesh(long ifc)
{
  /***********************************************************************/

  /***** Read points *****************************************************/

  ReadOFPoints(ifc);

  /***********************************************************************/

  /***** Read faces ******************************************************/

  ReadOFFaces(ifc);

  /************************************************************************/

  /***** Read owner and neighbour files **********************************/

  ReadOFOwnNbr(ifc);

  /***********************************************************************/

  /***** Create cell structures ******************************************/

  CreateUMSHCells(ifc);

  /***********************************************************************/

  /***** Read materials **************************************************/

  ReadOFMaterials(ifc);

  /***********************************************************************/

  /***** Read output mapping *********************************************/

  ReadOFMapping(ifc);

  /***********************************************************************/

  /***** Error check and finalize ****************************************/

  FinalizeUMSH(ifc);

  /***********************************************************************/
}

